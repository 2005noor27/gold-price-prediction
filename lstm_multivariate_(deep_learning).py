import pandas as pd
import numpy as np
from sklearn.preprocessing import MinMaxScaler

# Example: assume your dataframe is 'df' with columns like:
# ['Date', 'gold_price', 'feature1', 'feature2', ...]

# 1. Convert 'date' to numerical format
df['Date'] = pd.to_datetime(df['Date'])
df['date_ordinal'] = df['Date'].map(lambda x: x.toordinal())

# 2. Select features (date + other numerical features)
features = ['date_ordinal', 'Volume_Gold', 'Change%_Gold', 'Price_Oil',
       'Price_Dollar', 'Volume_Dollar', 'Price_Stocks', 'Volume_Stocks']
target = 'Price_Gold'

X = df[features].values
y = df[target].values.reshape(-1, 1)

# 3. Scale features
scaler_X = MinMaxScaler()
scaler_y = MinMaxScaler()

X_scaled = scaler_X.fit_transform(X)
y_scaled = scaler_y.fit_transform(y)

# 4. Reshape for LSTM: (samples, timesteps, features)
# For simplicity, we use 1 timestep per sample
X_scaled = X_scaled.reshape((X_scaled.shape[0], 1, X_scaled.shape[1]))

def create_sequences(X, y, seq_len=30):
    """
    X: scaled features (num_samples, num_features)
    y: scaled target (num_samples,)
    seq_len: number of past timesteps to use
    """
    X_seq, y_seq = [], []
    for i in range(len(X) - seq_len):
        X_seq.append(X[i:i+seq_len])
        y_seq.append(y[i + seq_len])
    return np.array(X_seq), np.array(y_seq)

# Create sequences
X_seq, y_seq = create_sequences(X_scaled.reshape(X_scaled.shape[0], X_scaled.shape[2]), y_scaled, seq_len=30)

# Now X_seq shape: (num_samples, 30, num_features)
# y_seq shape: (num_samples, 1)
print(X_seq.shape, y_seq.shape)

from tensorflow.keras.layers import Input

model = Sequential([
    Input(shape=(X_seq.shape[1], X_seq.shape[2])),  # تحديد شكل المدخلات بطريقة Keras الصحيحة
    LSTM(128, return_sequences=True),
    Dropout(0.2),
    LSTM(64),
    Dropout(0.2),
    Dense(32, activation='relu'),
    Dense(1)
])

history = model.fit(
    X_seq, y_seq,
    epochs=40,          # number of passes through the data
    batch_size=32,      # number of samples per gradient update
    validation_split=0.2,  # 20% of data for validation
    verbose=1
)

