def clean_currency(x):
    if isinstance(x, str):
        return float(x.replace(',', ''))
    return x

def clean_percent(x):
    if isinstance(x, str):
        return float(x.replace('%', '')) / 100
    return x

def clean_volume(x):
    if isinstance(x, str):
        x = x.upper()
        if 'K' in x:
            return float(x.replace('K', '')) * 1000
        elif 'M' in x:
            return float(x.replace('M', '')) * 1000000
        elif 'B' in x:
            return float(x.replace('B', '')) * 1000000000
        else:
            return float(x.replace(',', ''))
    return x

