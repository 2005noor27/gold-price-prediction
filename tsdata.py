df = pd.read_csv("TSDATA.csv")

"""**Basic Structure & Metadata**"""

print("Shape:", df.shape)
print("\nInfo:")
df.info()
print("\nMissing values:")
df.isna().sum()

"""**Convert Date to Datetime**"""

df['Date'] = pd.to_datetime(df['Date'], errors='coerce')

# Sort by date
df = df.sort_values('Date').reset_index(drop=True)

df[['Date']].head()

"""**Detect Columns Stored as Strings Instead of Numbers**"""

object_cols = df.select_dtypes(include='object').columns
object_cols

# Columns to convert
cols = ['Close_Gold', 'High_Gold', 'Low_Gold', 'Open_Gold',
        'Volume_Gold', 'Change%_Gold']

# 1) Remove commas
for c in cols:
    df[c] = df[c].astype(str).str.replace(',', '', regex=False)

# 2) Remove % from Change%_Gold
df['Change%_Gold'] = df['Change%_Gold'].str.replace('%', '', regex=False)

# 3) Convert all columns to numeric
for c in cols:
    df[c] = pd.to_numeric(df[c], errors='coerce')

df[cols].dtypes

df.info()

